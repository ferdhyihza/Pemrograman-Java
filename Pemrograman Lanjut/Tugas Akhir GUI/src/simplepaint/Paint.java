    public void paint(Graphics g) {
        super.paint(g);
        switch (color) {
            case "Putih"    : g.setColor(Color.white); break;
            case "Merah"    : g.setColor(Color.red); break;
            case "Kuning"   : g.setColor(Color.yellow); break;
            case "Hijau"    : g.setColor(Color.green); break;
            case "Hitam"    : g.setColor(Color.black); break;
            case "Biru"     : g.setColor(Color.blue); break;
            case "Pink"     : g.setColor(Color.pink); break;
            case "Oranye"   : g.setColor(Color.orange); break;
        }
        switch (shape) {
            case "Persegi"  : {
                if (points[0] > 0 & points[1] > 0 & points[2] > 0 & points[3] > 0) {
                    if (points[0] > points[2] && points[1] > points[3]) {
                    g.fillRect(points[2], points[3], Math.abs(x), Math.abs(y));
                } else if (points[0] < points[2] && points[1] > points[3]){
                    g.fillRect(points[0], points[3], Math.abs(x), Math.abs(y));
                } else if (points[0] > points[2] && points[1] < points[3]) {
                    g.fillRect(points[2], points[1], Math.abs(x), Math.abs(y));
                } else {
                    g.fillRect(points[0], points[1], Math.abs(x), Math.abs(y));
                    }
                }
            } break;
            case "Lingkaran": {
                int a=x;
                if (y>x) a=y;
                if (points[0] > 0 & points[1] > 0 & points[2] > 0 & points[3] > 0) {
                   if (points[0] > points[2] && points[1] > points[3]) {
                    g.fillOval(points[2], points[3], Math.abs(a), Math.abs(a));
                } else if (points[0] < points[2] && points[1] > points[3]){
                    g.fillOval(points[0], points[3], Math.abs(a), Math.abs(a));
                } else if (points[0] > points[2] && points[1] < points[3]) {
                    g.fillOval(points[2], points[1], Math.abs(a), Math.abs(a));
                } else {
                    g.fillOval(points[0], points[1], Math.abs(a), Math.abs(a));
                    } 
                }
            } break;
        }
        
    }


    private void BOARDMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BOARDMouseReleased
        // TODO add your handling code here:
        if (shape.equals("Persegi")){
            points[2] = 0;
            points[3] = 0;
            points[2] = evt.getX();
            points[3] = evt.getY();
            x = points[2] - points[0];
            y = points[3] - points[1];
            repaint();
        } else if (shape.equals("Lingkaran")) {
            points[2] = 0;
            points[3] = 0;
            points[2] = evt.getX();
            points[3] = evt.getY();
            x = points[2] - points[0];
            y = points[3] - points[1];
            repaint();
        }
    }//GEN-LAST:event_BOARDMouseReleased

